CREATE OR REPLACE FUNCTION public.set_ticket_organization_from_creator()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
begin
  if new.created_by is null then
    new.created_by := (select auth.uid());
  end if;

  if new.organization_id is null then
    select m.organization_id
      into new.organization_id
    from public.members m
    where m.user_id = new.created_by
    order by
      case m.role
        when 'ADMIN' then 1
        when 'MANAGER' then 2
        when 'AGENT' then 3
        else 4
      end,
      m.organization_id
    limit 1;
  end if;

  if new.organization_id is null then
    raise exception 'No organization membership found for user %', new.created_by;
  end if;

  return new;
end;
$function$
;


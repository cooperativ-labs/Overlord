const textExtensions = new Set([
  '.json',
  '.md',
  '.mdc',
  '.mjs',
  '.sh',
  '.svg',
  '.toml',
  '.txt',
  '.yml',
  '.yaml',
]);

async function pathExists(filePath) {
  try {
    await fs.access(filePath, fsConstants.F_OK);
    return true;
  } catch {
    return false;
  }
}

function isTextTemplate(filePath) {
  return textExtensions.has(path.extname(filePath));
}

function getTemplateValue(values, key) {
  return key.split('.').reduce((current, segment) => {
    if (current && typeof current === 'object' && segment in current) {
      return current[segment];
    }
    return undefined;
  }, values);
}

async function renderTemplate(content, agent, templatePath) {
  const values = agentConfigs[agent];
  if (!values) {
    throw new Error(`Missing agent config for ${agent}`);
  }

  const renderedIncludes = await replaceAsync(
    content,
    /\{\{>\s*([^}]+?)\s*\}\}/g,
    async (_match, includePath) => {
      const resolvedPath = path.resolve(path.dirname(templatePath), includePath.trim());
      const includedContent = await fs.readFile(resolvedPath, 'utf8');
      return renderTemplate(includedContent, agent, resolvedPath);
    }
  );

  const renderedConditionals = renderedIncludes.replaceAll(
    /\{\{#if\s+([a-zA-Z0-9_.-]+)\s*\}\}([\s\S]*?)\{\{\/if\s*\}\}/g,
    (_match, key, inner) => (getTemplateValue(values, key) ? inner : '')
  );

  return renderedConditionals.replaceAll(/\{\{\s*([a-zA-Z0-9_.-]+)\s*\}\}/g, (match, key) => {
    const value = getTemplateValue(values, key);
    return value === undefined ? match : String(value);
  });
}

async function replaceAsync(input, pattern, replacer) {
  const matches = Array.from(input.matchAll(pattern));
  if (matches.length === 0) {
    return input;
  }

  let result = '';
  let lastIndex = 0;
  for (const match of matches) {
    result += input.slice(lastIndex, match.index);
    result += await replacer(...match);
    lastIndex = match.index + match[0].length;
  }
  result += input.slice(lastIndex);
  return result;
}

async function listFiles(root) {
  const files = [];

  async function walk(current) {
    for (const entry of await fs.readdir(current, { withFileTypes: true })) {
      const fullPath = path.join(current, entry.name);
      if (entry.isDirectory()) {
        await walk(fullPath);
      } else if (entry.isFile()) {
        files.push(path.relative(root, fullPath));
      }
    }
  }

  await walk(root);
  return files.sort();
}

async function renderFile(sourceFile, targetFile, agent) {
  await fs.mkdir(path.dirname(targetFile), { recursive: true });
  const stat = await fs.stat(sourceFile);

  if (isTextTemplate(sourceFile)) {
    const rendered = await renderTemplate(await fs.readFile(sourceFile, 'utf8'), agent, sourceFile);
    await fs.writeFile(targetFile, rendered, { mode: stat.mode });
  } else {
    await fs.copyFile(sourceFile, targetFile);
    await fs.chmod(targetFile, stat.mode);
  }
}

async function renderAgent(agent, targetRoot) {
  const sourceDir = path.join(sourceRoot, agent);
  const targetDir = path.join(targetRoot, agent);

  if (!(await pathExists(sourceDir))) {
    throw new Error(`Missing source directory: ${path.relative(repoRoot, sourceDir)}`);
  }

  await fs.rm(targetDir, { recursive: true, force: true });
  await fs.mkdir(targetDir, { recursive: true });

  for (const relativeFile of await listFiles(sourceDir)) {
    await renderFile(path.join(sourceDir, relativeFile), path.join(targetDir, relativeFile), agent);
  }
}

async function snapshotAgent(agent, targetRoot) {
  const sourceDir = path.join(sourceRoot, agent);
  const files = new Map();

  for (const relativeFile of await listFiles(sourceDir)) {
    const sourceFile = path.join(sourceDir, relativeFile);
    const stat = await fs.stat(sourceFile);
    const contents = isTextTemplate(sourceFile)
      ? Buffer.from(await renderTemplate(await fs.readFile(sourceFile, 'utf8'), agent, sourceFile))
      : await fs.readFile(sourceFile);
    files.set(relativeFile, {
      contents,
      mode: stat.mode & 0o777,
      target: path.join(targetRoot, agent, relativeFile),
    });
  }

  return files;
}

async function validateJsonOutputs() {
  const failures = [];
  for (const targetRoot of targetRoots) {
    for (const agent of agents) {
      const agentRoot = path.join(targetRoot, agent);
      if (!(await pathExists(agentRoot))) continue;
      for (const relativeFile of await listFiles(agentRoot)) {
        if (!relativeFile.endsWith('.json')) continue;
        const fullPath = path.join(agentRoot, relativeFile);
        try {
          JSON.parse(await fs.readFile(fullPath, 'utf8'));
        } catch (error) {
          failures.push(
            `${path.relative(repoRoot, fullPath)}: ${
              error instanceof Error ? error.message : String(error)
            }`
          );
        }
      }
    }
  }
  return failures;
}

async function checkAgent(agent, targetRoot) {
  const expected = await snapshotAgent(agent, targetRoot);
  const targetDir = path.join(targetRoot, agent);
  const actualFiles = (await pathExists(targetDir)) ? await listFiles(targetDir) : [];
  const actualSet = new Set(actualFiles);
  const failures = [];

  for (const relativeFile of actualFiles) {
    if (!expected.has(relativeFile)) {
      failures.push(`unexpected ${path.relative(repoRoot, path.join(targetDir, relativeFile))}`);
    }
  }

  for (const [relativeFile, expectedFile] of expected) {
    const displayPath = path.relative(repoRoot, expectedFile.target);

    if (!actualSet.has(relativeFile)) {
      failures.push(`missing ${displayPath}`);
      continue;
    }

    const actual = await fs.readFile(expectedFile.target);
    if (!actual.equals(expectedFile.contents)) {
      failures.push(`content drift ${displayPath}`);
    }

    const actualMode = (await fs.stat(expectedFile.target)).mode & 0o777;
    if (actualMode !== expectedFile.mode) {
      failures.push(
        `mode drift ${displayPath} expected ${expectedFile.mode.toString(8)} got ${actualMode.toString(8)}`
      );
    }
  }

  return failures;
}

async function main() {
  if (mode === 'render') {
    for (const targetRoot of targetRoots) {
      for (const agent of agents) {
        await renderAgent(agent, targetRoot);
      }
    }
    console.log('Rendered Claude, Cursor, and Codex plugins into plugins/ and packages/overlord-cli/plugins/.');
    return;
  }

  const failures = [];
  for (const targetRoot of targetRoots) {
    for (const agent of agents) {
      failures.push(...(await checkAgent(agent, targetRoot)));
    }
  }

  if (failures.length > 0) {
    console.error('Agent plugin output drift detected. Run `yarn plugins:render` and commit the result.');
    for (const failure of failures) {
      console.error(`- ${failure}`);
    }
    process.exit(1);
  }

  const jsonFailures = await validateJsonOutputs();
  if (jsonFailures.length > 0) {
    console.error('Rendered JSON files do not parse. Fix the templates or agent config.');
    for (const failure of jsonFailures) {
      console.error(`- ${failure}`);
    }
    process.exit(1);
  }

  console.log('Agent plugin outputs match plugins/_source and parse as JSON.');
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
